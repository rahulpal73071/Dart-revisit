void main() {
  // arithmatic (+,-.*,/,~/,%)
  var a = 23;
  var b = 654;
  print(a + b);
  print(b - a);
  print(a * b);
  print(b / a);
  print(b % a);
  print(b ~/ a);
  // assignment(=,==,+=,-=,*= ,/=)
  print(a);
  a += 5;
  print(a);
  a -= 5;
  print(a);
  // logical(&& , || , !)
  print(true && false);
  print(true && true);
  print(false || true);
  print(!true);
}
